function translate(text) {
  if (dict[text]) return dict[text]

  let out = text
  const rules = [
    [/^Failed to (.+)$/, 'No se pudo $1'],
    [/^Could not (.+)$/, 'No se pudo $1'],
    [/^Couldn(?:'|')t (.+)$/, 'No se pudo $1'],
    [/^Loading (.+)\.\.\.$/, 'Cargando $1…'],
    [/^Loading (.+)…$/, 'Cargando $1…'],
    [/^Open (.+)$/, 'Abrir $1'],
    [/^Close (.+)$/, 'Cerrar $1'],
    [/^Show (.+)$/, 'Mostrar $1'],
    [/^Hide (.+)$/, 'Ocultar $1'],
    [/^Search (.+)$/, 'Buscar $1'],
    [/^Delete (.+)$/, 'Eliminar $1'],
    [/^Remove (.+)$/, 'Eliminar $1'],
    [/^Save (.+)$/, 'Guardar $1'],
    [/^Create (.+)$/, 'Crear $1'],
    [/^Edit (.+)$/, 'Editar $1'],
    [/^Update (.+)$/, 'Actualizar $1'],
    [/^Refresh (.+)$/, 'Actualizar $1'],
    [/^Copy (.+)$/, 'Copiar $1'],
    [/^Enable (.+)$/, 'Habilitar $1'],
    [/^Disable (.+)$/, 'Deshabilitar $1'],
    [/^Connect (.+)$/, 'Conectar $1'],
    [/^Sign in to (.+)$/, 'Iniciar sesión en $1'],
    [/^Sign in with (.+)$/, 'Iniciar sesión con $1'],
    [/^No (.+) found\.?$/, 'No se encontró $1'],
    [/^No (.+) yet\.?$/, 'Aún no hay $1'],
    [/^(.+) failed\.?$/, 'Error en $1'],
    [/^(.+) failed to load\.?$/, 'No se pudo cargar $1'],
    [/^(.+) failed to start\.?$/, 'No se pudo iniciar $1'],
    [/^(.+) settings saved\.?$/, 'Configuración de $1 guardada'],
    [/^(.+) saved\.?$/, '$1 guardado'],
    [/^(.+) deleted\.?$/, '$1 eliminado'],
    [/^(.+) created\.?$/, '$1 creado'],
    [/^(.+) updated\.?$/, '$1 actualizado'],
    [/^(.+) enabled\.?$/, '$1 habilitado'],
    [/^(.+) disabled\.?$/, '$1 deshabilitado'],
    [/^(.+) required\.?$/, '$1 requerido'],
    [/^(.+) unavailable\.?$/, '$1 no disponible']
  ]

  for (const [re, repl] of rules) {
    const m = out.match(re)
    if (m) {
      out = out.replace(re, repl)
      break
    }
  }

  return out
}

const enText = fs.readFileSync(path.join(__dirname, 'en.ts'), 'utf8')
const allEnStrings = []
const re = /:\s*'((?:\\'|[^'])*)'/g
let m
while ((m = re.exec(enText)) !== null) {
  allEnStrings.push(m[1].replace(/\\'/g, "'"))
}

const map = Object.fromEntries(allEnStrings.map(s => [s, translate(s)]))
fs.writeFileSync(path.join(__dirname, '_es-map.json'), JSON.stringify(map, null, 2), 'utf8')

const untranslated = Object.entries(map).filter(([k, v]) => k === v && /[A-Za-z]/.test(k))
console.log('total', allEnStrings.length, 'untranslated-ish', untranslated.length)
